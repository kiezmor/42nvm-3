int		ft_str_is_alpha(char *str)
{
	int i;
	int	j;

	i = 0;
	while (str[i] != '\0')
	{
		j = i;
		while (str[i] >= 97 && str[i] <= 122)
			i++;
		while (str[i] >= 65 && str[i] <= 90)
			i++;
		if (j == i)
			return (0);
	}
	return (1);
}
