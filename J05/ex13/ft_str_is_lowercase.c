int		ft_str_is_lowercase(char *str)
{
	int	i;
	int	j;

	i = 0;
	while (str[i] != '\0')
	{
		j = i;
		while (str[i] >= 97 && str[i] <= 122)
			i++;
		if (j == i)
			return (0);
	}
	return (1);
}
