char	*ft_strncat(char *dest, char *src, int nb)
{
	int v;
	int p;

	v = 0;
	p = 0;
	while (dest[v] != '\0')
		v++;
	while (src[p] != '\0' && p < nb)
	{
		dest[v + p] = src[p];
		p++;
	}
	dest[v + p] = '\0';
	return (dest);
}
